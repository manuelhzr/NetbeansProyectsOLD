    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Date fechaaux=Fecha.getDate();
        LocalDate FechaACT= fechaaux.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        obj.setNacimiento(FechaACT);
        edad_res.setText(String.valueOf(obj.edad()));
        
        obj.setNombre(nombre.getText());
        obj.setEps(EPS.getText());
        obj.setSexo(sexo.getSelectedItem().toString());
        obj.setPeso(Float.parseFloat(peso.getText()));
        obj.setEstatura(Float.parseFloat(estatura.getText()));
        obj.setGlucosa(Float.parseFloat(glucosa.getText()));
        obj.setHemoglobina(Float.parseFloat(hemoblobina.getText()));
        
        
        imc_res.setText(String.valueOf(obj.IMC()));
        clasificacion_res.setText(obj.clasificacionIMC());
        glucosa_res.setText(obj.nivelGlucosa());
        hemoglobina_res.setText(obj.nivelHemoglobina());
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

