    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (pos<obj.getTam()) {
            datosPeliculas aux=new datosPeliculas();
            aux.setTitulo(titulo.getText());
            aux.setGenero(genero.getSelectedItem().toString());
            aux.setDuracion(Float.parseFloat(duracion.getText()));
            aux.setCosto(Float.parseFloat(costo.getText()));
            aux.setPuntuacion(Integer.parseInt(puntuacion.getSelectedItem().toString()));
            
            obj.setVector(pos, aux);
            pos++;
        }else{
            JOptionPane.showMessageDialog(rootPane, "Se alcanzo el limite de facturas");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

