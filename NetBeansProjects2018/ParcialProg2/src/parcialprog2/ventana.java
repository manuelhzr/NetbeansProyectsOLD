    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String dat=campoBuscar.getText();
        int res=obj.BusquedaBinaria(dat);
        
        if (res!=-1) {
            TitBuscar.setText(obj.getVector(pos).getTitulo());
            GenBuscar.setText(obj.getVector(pos).getGenero());
            durBuscar.setText(String.valueOf(obj.getVector(pos).getDuracion()));
            CostoBuscar.setText(String.valueOf(obj.getVector(pos).getCosto()));
            PuntBusca.setText(String.valueOf(obj.getVector(pos).getPuntuacion()));
        }else{
            JOptionPane.showMessageDialog(rootPane, "La pelicula no existe");
            
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed

