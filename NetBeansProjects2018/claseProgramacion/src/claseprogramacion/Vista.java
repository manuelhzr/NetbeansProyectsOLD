    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        float n=Float.parseFloat(valNotas.getText());
        if (n>=0 && n<=5) {
            if (pos<obj.getCantidadNotas()) {
                obj.setVectorNotas(pos, n);
                pos++;
                valNotas.setText("");
            }else{
                JOptionPane.showMessageDialog(rootPane, "Se alcanzo el limite de notas");
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "La nota no es valida");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

