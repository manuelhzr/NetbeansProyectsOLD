    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        if (posAct<50) {
            temp=new datosFactura();
            temp.setCodigo(Integer.parseInt(codigoAgreg.getText()));
            temp.setCliente(clienteAgreg.getText());
            temp.setTipoVehiculo(tipovehAgreg.getSelectedItem().toString());
            temp.setNumHoras(Integer.parseInt(HorasAgreg.getText()));
            if (temp.getTipoVehiculo().equals("Auto")) {
                temp.setValor((valAuto*temp.getNumHoras()));
            }else{
                if (temp.getTipoVehiculo().equals("Camioneta")) {
                temp.setValor((valCamioneta*temp.getNumHoras()));
                }else{
                    if (temp.getTipoVehiculo().equals("Bus")) {
                    temp.setValor((valBus*temp.getNumHoras()));
                    }
                }
            }
            
            obj.setFacturas(posAct, temp);
            posAct++;
            JOptionPane.showMessageDialog(rootPane, "Factura agregada");
            
            codigoAgreg.setText("");
            clienteAgreg.setText("");
            tipovehAgreg.setSelectedIndex(0);
            HorasAgreg.setText("");
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "Se alcanzo el limite de facturas");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

