    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        int pos;
        int dato=Integer.parseInt(campobusqueda.getText());
        if (TipoBusqueda.getSelectedItem().toString().equals("Lineal")) {
            pos=metodos.BusquedaLineal(obj.getFacturas(), dato);
        }else{
            pos=metodos.BusquedaBinaria(obj.getFacturas(), dato);
        }
        
        if (pos!=-1) {
            codigobuscar.setText(String.valueOf(obj.getPos(pos).getCodigo()));
            clientebuscar.setText(obj.getPos(pos).getCliente());
            tipovehiculobuscar.setText(obj.getPos(pos).getTipoVehiculo());
            numerohbuscar.setText(String.valueOf(obj.getPos(pos).getNumHoras()));
            valorbuscar.setText(String.valueOf(obj.getPos(pos).getNumHoras()));
        }else{
            JOptionPane.showMessageDialog(rootPane, "La factura no se encuentra registrada");
        }
        
    }//GEN-LAST:event_jButton7ActionPerformed

