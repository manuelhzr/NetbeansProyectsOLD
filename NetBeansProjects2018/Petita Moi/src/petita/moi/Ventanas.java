    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        String v=ComboOrdenar.getSelectedItem().toString();
        if (v.equals("Burbuja")) {
            metodos.burbuja(obj.getFacturas());
        }else{
            if (v.equals("Intercambio")) {
                metodos.intercambio(obj.getFacturas());
            }else{
                if (v.equals("Quicksort")) {
                    metodos.quickSort(obj.getFacturas(), 0, 50);
                }
            }
        }
        
        JOptionPane.showMessageDialog(rootPane, "Datos ordenados");
        ordenarVentana.setVisible(false);
    }//GEN-LAST:event_jButton9ActionPerformed

