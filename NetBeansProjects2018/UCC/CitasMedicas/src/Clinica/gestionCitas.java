    private void limpiar(){
        nombreTextfield.setText("");
        idTextfield.setText("");
        especialidadCombobox.setSelectedIndex(0);
        fechaTextfield.setText("");
    }
    
    private void mostrar(){
        int aux=0;
        for (int i = 0; i < 100; i++) {
            if (adminClinica.getAdmin().getListaCitas(i)!=null) {
                tablamostrar.setValueAt(adminClinica.getAdmin().getListaCitas(i).getNombre(), aux, 0);
                tablamostrar.setValueAt(adminClinica.getAdmin().getListaCitas(i).getId(), aux, 1);
                tablamostrar.setValueAt(adminClinica.getAdmin().getListaCitas(i).getEspecialidad(), aux, 2);
            }
        }
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
         Prototype prt=new Prototype();
        String nombre=nombreTextfield.getText();
        int id=Integer.parseInt(idTextfield.getText());
        String especialidad=especialidadCombobox.getSelectedItem().toString();
        String fecha=fechaTextfield.getText();
        prt.setPac("", fecha, nombre, id, especialidad);
        try {
            
            pacientes pac= (pacientes) prt.clonar("Paciente");
            adminClinica.getAdmin().agregarCita(pac);
            HistorialCitas.getHis().setHistoria(pac);
            JOptionPane.showMessageDialog(rootPane, "Cita registrada");
            limpiar();
            registrar.setVisible(false);
            mostrar();
            
        } catch (CloneNotSupportedException ex) {
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

