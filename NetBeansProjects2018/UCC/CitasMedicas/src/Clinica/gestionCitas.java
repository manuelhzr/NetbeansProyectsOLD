    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int id=Integer.parseInt(JOptionPane.showInputDialog("Ingrese el ID del paciente para cancelar cita"));
        int res=adminClinica.getAdmin().buscarCita(id);
        if (res!=-1) {
            int r=JOptionPane.showConfirmDialog(rootPane, "Â¿Desea eliminar la cita?", "Eliminar", 0);
            if (r==0) {
                adminClinica.getAdmin().setListaCitas(res, null);
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "La cita no se encuentra");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

