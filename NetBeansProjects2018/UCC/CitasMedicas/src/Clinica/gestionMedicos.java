    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int id=Integer.parseInt(JOptionPane.showInputDialog("Ingrese el ID del medico a eliminar"));
        int res=adminClinica.getAdmin().buscarMedico(id);
        if (res!=-1) {
            int r=JOptionPane.showConfirmDialog(rootPane, "Â¿Desea eliminar el medico?", "Eliminar", 0);
            if (r==0) {
                adminClinica.getAdmin().setListaMedicos(res, null);
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "El medico no se encuentra");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void mostrar(){
        int aux=0;
        for (int i = 0; i < 100; i++) {
            if (adminClinica.getAdmin().getListaMedicos(i)!=null) {
                tablaMostrar.setValueAt(adminClinica.getAdmin().getListaMedicos(i).getNombre(), aux, 0);
                tablaMostrar.setValueAt(adminClinica.getAdmin().getListaMedicos(i).getId(), aux, 1);
                tablaMostrar.setValueAt(adminClinica.getAdmin().getListaMedicos(i).getEspecialidad(), aux, 2);
                aux++;
            }
        }
    }
    
