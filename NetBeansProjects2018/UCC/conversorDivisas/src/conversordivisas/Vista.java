    private void cantidadKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cantidadKeyReleased
        // TODO add your handling code here:
        
        try {
            if (!cantidad.getText().equals("")) {
                Double.parseDouble(cantidad.getText());
            }             
        } catch (NumberFormatException e) {
            
            JOptionPane.showMessageDialog(rootPane, "Debe registrar un valor numerico", "Error", JOptionPane.ERROR_MESSAGE, null);
            
            cantidad.setText("");
            
        }
    }//GEN-LAST:event_cantidadKeyReleased

