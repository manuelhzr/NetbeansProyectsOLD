    private boolean comprobar(){
        boolean valido=true;
        if(nombre.getText().equals("") || identificacion.getText().equals("") || direccion.getText().equals("") || telefono.getText().equals("") ||
            correo.getText().equals("") || Ncuenta.getText().equals("") || Saldo.getText().equals("") || movimientos.getText().equals("")){
           
            valido=false;
            
        }
        
        return valido;
    }
    
    private void limpiar(){
        nombre.setText("");
        identificacion.setText("");
        direccion.setText("");
        telefono.setText("");
        correo.setText("");
        Ncuenta.setText("");
        Saldo.setText("");
        movimientos.setText("");
    }
    
