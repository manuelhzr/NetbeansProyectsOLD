    private void RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarActionPerformed
        // TODO add your handling code here:       
        RegistrarCliente ob=princ.Regis();
        if(comprobar()==true){
            client=new CLIENTE();
            client.setNombre(nombre.getText());
            client.setIdcliente(Integer.parseInt(identificacion.getText()));
            client.setDireccion(direccion.getText());
            client.setTelefono(Integer.parseInt(telefono.getText()));
            client.setCorreo(correo.getText());
            
            cuent=new CUENTA();
            
            cuent.setDatoscliente(client);
            cuent.setNrocuenta(Integer.parseInt(Ncuenta.getText()));
            cuent.setTipocuenta(TCuenta.getSelectedItem().toString());
            cuent.setSaldo(Float.parseFloat(Saldo.getText()));
            cuent.setNmovimiento(Integer.parseInt(movimientos.getText()));
            
            if (princ.Banco.Insertarcuenta(cuent)==true) {
                JOptionPane.showMessageDialog(rootPane, "Cliente Registrado Exitosamente");
                limpiar();
                ob.setVisible(false);
            }else{
                JOptionPane.showMessageDialog(rootPane, "Error al registrar el cliente");
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Debe llenar todos los campos");
        }
        
        
    }//GEN-LAST:event_RegistrarActionPerformed

