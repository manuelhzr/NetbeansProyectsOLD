    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int bus=Integer.parseInt(Busqueda.getText());
        resultado=obj.Banco.Buscar(bus);
        if (resultado!=-1) {
            CUENTA res=obj.Banco.getListacuenta(resultado);
            Numero.setText(String.valueOf(res.getNrocuenta()));
            Titular.setText(res.getDatoscliente().getNombre());
            Saldo.setText(String.valueOf(res.getSaldo()));
            Tipo.setText(res.getTipocuenta());
            habilitar(true);
        }else{
            JOptionPane.showMessageDialog(rootPane, "El numero de cuenta ingresado no se encuentra");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void BorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BorrarActionPerformed
        // TODO add your handling code here:
        int r=JOptionPane.showConfirmDialog(rootPane, "Â¿Desea eliminar la cuenta seleccionada?", "Confirmar", JOptionPane.YES_NO_OPTION);
        if (r == 0) {
            if (obj.Banco.Eliminarcuenta(resultado) == true) {
                JOptionPane.showMessageDialog(rootPane, "La cuenta ha sido eliminada");
                limpiar();
            }else{
                JOptionPane.showMessageDialog(rootPane, "La cuenta no ha sido eliminada");
            }
        }
    }//GEN-LAST:event_BorrarActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        limpiar();
        Eliminar ob=obj.borr();
        ob.setVisible(false);
    }//GEN-LAST:event_jButton3ActionPerformed

    public void habilitar(boolean v){
        if (v==true) {
            Borrar.setEnabled(v);
        }else{
            Borrar.setEnabled(v);
        }
    }
    
    private void limpiar(){
        Numero.setText("");
        Titular.setText("");
        Saldo.setText("");
        Tipo.setText("");
        Busqueda.setText("");
    }
    
