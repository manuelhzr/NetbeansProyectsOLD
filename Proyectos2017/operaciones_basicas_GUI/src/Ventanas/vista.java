    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int num1 = Integer.parseInt(text_num1.getText());
        int num2 = Integer.parseInt(text_num2.getText());
        int operacion = cbox_operacion.getSelectedIndex();
        operacionesBasicas obj = new operacionesBasicas(num1, num2);
        switch(operacion){
            case 0:
                label_resultado.setText(num1+" + "+num2 +" = "+obj.suma());
                break;
            case 1:
                label_resultado.setText(num1+" - "+num2 +" = "+obj.resta());
                break;
            case 2:
                label_resultado.setText(num1+" x "+num2 +" = "+obj.multiplicacion());
                break;
            case 3:
                label_resultado.setText(num1+" / "+num2 +" = "+obj.division());
                break;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

