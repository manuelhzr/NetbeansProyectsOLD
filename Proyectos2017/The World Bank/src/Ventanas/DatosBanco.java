    public void mostrar(){
        if (datos.Banco.getNelementos()!=0) {
            clientes.setEnabled(false);
            cancelar.setEnabled(true);
        }else{
            cancelar.setEnabled(false);
        }
        gerente.setText(datos.Banco.getNombregerente());
        direccion.setText(datos.Banco.getDireccion());
        telefono.setText(datos.Banco.getTelefono());
        correo.setText(datos.Banco.getCorreo());
        clientes.setText(String.valueOf(datos.Banco.getNelementos()));
        
    }
    
    
