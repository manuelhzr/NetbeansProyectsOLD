    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        datos.Banco.setNombregerente(gerente.getText());
        datos.Banco.setDireccion(direccion.getText());
        datos.Banco.setTelefono(telefono.getText());
        datos.Banco.setCorreo(correo.getText());
        if(datos.Banco.getNelementos()==0){
            datos.Banco.setNelementos(Integer.parseInt(clientes.getText()));
        }
        datos.MostrarDatos();
        DatosBanco o=datos.ob();
        o.setVisible(false);
        
    }//GEN-LAST:event_jButton1ActionPerformed

