    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
            String cat=BoxCategoria.getSelectedItem().toString().toLowerCase();
            int fila = t_datos.getSelectedRow();
            if(fila<0){
                JOptionPane.showMessageDialog(null, "No se encontro seleccion \n\n Favor seleccione el mensaje que desea eliminar");
            }else{
            String cod =  t_datos.getValueAt(fila, 0).toString();
            try {
                PreparedStatement pst = cn.prepareStatement("DELETE FROM "+cat+" WHERE cod_me='"+cod+"'");
                pst.executeUpdate();
                cc2.cargar("",cat );
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error: " + ex);
            }}
       
    }//GEN-LAST:event_jMenuItem2ActionPerformed


