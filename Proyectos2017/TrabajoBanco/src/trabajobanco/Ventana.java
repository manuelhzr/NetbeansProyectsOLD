    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        int parametro=Integer.parseInt(JOptionPane.showInputDialog("Ingrese el id del cliente a consultar"));
        int res=obj.busqueda(parametro);
        if (res!=-1){
            int id=Integer.parseInt(JOptionPane.showInputDialog("Ingrese el id del movimiento a consultar"));
            Movimiento result=obj.consultarmovimiento(pos, id);
            if (result!=null) {
                JOptionPane.showMessageDialog(rootPane, "ID del movimiento: " + result.getIdmoviento() + "\n"
                        + "Fecha: " + result.getFecha() + "\n"
                        + "Tipo: " + result.getTipo() + "\n"
                        + "Nombre de quien realizo: " + result.getQuienRealiza() + "\n"
                        + "Valor: " + result.getValor() + "\n");
            }else{
                JOptionPane.showMessageDialog(rootPane, "No encontro el movimientro ingresado");
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "La cuenta no se encuentra registrada");
        }
    }//GEN-LAST:event_jButton8ActionPerformed

