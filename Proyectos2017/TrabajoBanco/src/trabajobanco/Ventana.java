    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (pos < obj.getNelementos()) {
            client = new Clientes();
            cuent=new Cuenta();
            client.setNombre(JOptionPane.showInputDialog("Ingrese el nombre del cliente"));
            client.setIdcliente(Integer.parseInt(JOptionPane.showInputDialog("Ingrese el id del cliente")));
            client.setCorreo(JOptionPane.showInputDialog("Ingrese el correo del cliente"));
            client.setDireccion(JOptionPane.showInputDialog("Ingrese la direccion del cliente"));
            client.setTelefono(JOptionPane.showInputDialog("ingrese el telefono del cliente"));
            
            cuent.setDatoscliente(client);
            cuent.setTipocuenta(JOptionPane.showInputDialog("ingrese el tipo de cuenta"));
            cuent.setNumcuenta(Integer.parseInt(JOptionPane.showInputDialog("Ingrese el numero de cuenta")));
            cuent.setSaldo(Float.parseFloat(JOptionPane.showInputDialog("Ingrese el saldo inicial")));
            cuent.setNmovimiento(Integer.parseInt(JOptionPane.showInputDialog("Ingrese el maximo numero de movimientos permitidos")));
            
            obj.insertarcuenta(pos, cuent);
            pos++;
        }else{
            JOptionPane.showMessageDialog(rootPane, "Se alcanzo el maximo numero de clientes permitidos");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int parametro=Integer.parseInt(JOptionPane.showInputDialog("Ingrese el id del cliente a eliminar"));
        int res=obj.busqueda(parametro);
        if (res!=-1) {
            obj.eliminar(pos);
            JOptionPane.showMessageDialog(rootPane, "La cuenta ha sido eliminada");
        }else{
            JOptionPane.showMessageDialog(rootPane, "La cuenta no se encuentra registrada");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int parametro=Integer.parseInt(JOptionPane.showInputDialog("Ingrese el id del cliente al que desea consignar"));
        int res=obj.busqueda(parametro);
        if (res!=-1 && obj.getListaCuenta(res).getPosicionactual()<obj.getListaCuenta(res).getNmovimiento()) {
            mov=new Movimiento();
            float saldo=Float.parseFloat(JOptionPane.showInputDialog("Ingrese la cantidad a consignar"));
            mov.setFecha(JOptionPane.showInputDialog("Ingrese la fecha"));
            mov.setIdmoviento(Integer.parseInt(JOptionPane.showInputDialog("Ingrese el id del movimiento")));
            mov.setTipo("Consigancion");
            mov.setQuienRealiza(JOptionPane.showInputDialog("Ingrese el nombre de quien realiza"));
            mov.setValor(saldo);
            
            obj.getListaCuenta(res).Consignar(saldo, mov);
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "La cuenta no se encuentra registrada");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

