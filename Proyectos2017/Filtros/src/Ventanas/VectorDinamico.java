    private FiltroWhithArray Obj;
    
    private void refresh(){
        Numeros.clear();
        for (int i = 0; i < Obj.GetTamaño(); i++) {
            Numeros.addElement(Obj.Obtener(i));
        }
    }
    
    private void Pares(){
        Filtros.clear();
        boolean Validar;
        for (int i = 0; i < Obj.GetTamaño(); i++) {
            Validar = Obj.isPar(Obj.Obtener(i));
            if(Validar){
                Filtros.addElement(Obj.Obtener(i));
            }
        }
    }
    
    private void Impares(){
        Filtros.clear();
        boolean Validar;
        for (int i = 0; i < Obj.GetTamaño(); i++) {
            Validar = Obj.isImpar(Obj.Obtener(i));
            if(Validar){
                Filtros.addElement(Obj.Obtener(i));
            }
        }
    }
    
    private void Primos(){
        Filtros.clear();
        boolean Validar;
        for (int i = 0; i < Obj.GetTamaño(); i++) {
            Validar = Obj.isPrimo(Obj.Obtener(i));
            if(Validar){
                Filtros.addElement(Obj.Obtener(i));
            }
        }
    }
    
    private void MultiplosX(){
        int Num= Integer.parseInt(JOptionPane.showInputDialog("Ingrese el Numero"));
        Filtros.clear();
        boolean Validar;
        for (int i = 0; i < Obj.GetTamaño(); i++) {
            Validar = Obj.isMultipleX(Num, Obj.Obtener(i));
            if(Validar){
                Filtros.addElement(Obj.Obtener(i));
            }
        }
    }
    
    private void AplicoFiltro(){
        if(Filtros.isEmpty()){
            JOptionPane.showMessageDialog(this, "El filtro no aplico nungun cambio");
        }
    }

