    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:
        int Seleccion = jList1.getSelectedIndex();
        if(Seleccion>-1){
            int Num= Integer.parseInt(JOptionPane.showInputDialog("Modificando el elemento ["+Seleccion+"] \n"));
            Obj.Modificar(Seleccion, Num);
            refresh();
            jList1.setSelectedIndex(Seleccion);
        }else{
            JOptionPane.showMessageDialog(this, "No ha seleccionado ningun elemento de la lista");            
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        // TODO add your handling code here:
        int Seleccion = jList1.getSelectedIndex();
        if(Seleccion>-1){
            int resp=JOptionPane.showConfirmDialog(this, "Esta seguro de eliminar el elemento ["+Seleccion+"] \n", "Elinar", JOptionPane.YES_NO_OPTION);
            if(resp==JOptionPane.YES_OPTION){
                Obj.Eliminar(Seleccion);
                refresh();
            }
        }else{
            JOptionPane.showMessageDialog(this, "No ha seleccionado ningun elemento de la lista");
        }
        
    }//GEN-LAST:event_jMenuItem2ActionPerformed

