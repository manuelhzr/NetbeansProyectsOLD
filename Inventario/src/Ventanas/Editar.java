    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            int codigo = Integer.parseInt(jTextField1.getText());

            Producto resultado = Inventario.datosInventario.buscar(codigo);
            pos = Inventario.datosInventario.buscarCod(codigo);

            if (resultado != null) {
                jTextField2.setText(String.valueOf(resultado.getCodigo()));
                jTextField3.setText(resultado.getNombre());
                jTextField4.setText(String.valueOf(resultado.getCantidad()));
                jTextField5.setText(String.valueOf(resultado.getPrecioUnitario()));
                

            } else {
                JOptionPane.showMessageDialog(rootPane, "Producto no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Codigo no Valido", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
        int codigo = Integer.parseInt(jTextField2.getText());
        String nombre = jTextField3.getText();
        int cantidad = Integer.parseInt(jTextField4.getText());
        double valor = Double.parseDouble(jTextField5.getText());
        
        Producto nuevo = new Producto(codigo, nombre, cantidad, valor);
        
        Inventario.datosInventario.setListaProductos(pos, nuevo);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Datos no Validos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

