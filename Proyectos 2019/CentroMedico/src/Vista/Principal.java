    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el ID del especialista a consultar: "));
        try {
            String info=Objetos.archivoMedicos.consultarInformacion(id);
            if (!info.equals("")) {
                String mostrar="Informacion solicitada:\n\n";
                StringTokenizer datos = new StringTokenizer(info, "\t");
                while (datos.hasMoreElements()) {                    
                    mostrar+=datos.nextToken()+"\n";
                }
                JOptionPane.showMessageDialog(rootPane, mostrar);
            }else{
                JOptionPane.showMessageDialog(rootPane, "No se encuentra el ID");
            }
        } catch (IOException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, "Error");
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed

