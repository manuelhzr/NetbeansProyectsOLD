    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        float Distancia= Float.parseFloat(jTextField1.getText());
        int NumEscalas= Integer.parseInt(jTextField2.getText());
        String Transporte = jTextField3.getText();
        String tipo= jComboBox2.getSelectedItem().toString();
        if (jComboBox1.getSelectedIndex()==0) {
            
            Tierra aux= new Tierra();
            aux.setDistanciaDestino(Distancia);
            aux.setNumEscalas(NumEscalas);
            aux.setTranportadora(Transporte);
            aux.setTipoBus(tipo);
            
            jTextField4.setText(String.valueOf(aux.ValorDistancia()));
            jTextField5.setText(String.valueOf(aux.CargoEscala()));
            jTextField6.setText(String.valueOf(aux.CargoTipo()));
            jTextField7.setText(String.valueOf(precios.CalcularTierra(aux)));
            
        }else{
            Aire aux = new Aire();
            aux.setDistanciaDestino(Distancia);
            aux.setNumEscalas(NumEscalas);
            aux.setAerolinea(Transporte);
            aux.setClase(tipo);
            
            jTextField4.setText(String.valueOf(aux.ValorDistancia()));
            jTextField5.setText(String.valueOf(aux.CargoEscala()));
            jTextField6.setText(String.valueOf(aux.CargoTipo()));
            jTextField7.setText(String.valueOf(precios.CalcularAire(aux)));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

