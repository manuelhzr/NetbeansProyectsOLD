    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        int val=Integer.parseInt(JOptionPane.showInputDialog("Ingrese el valor a buscar"));
        
        int res=obj.busquedaLineal(val);
        if (res!=-1) {
            JOptionPane.showMessageDialog(rootPane, "El valor se encuentra en la poscion: "+res);
        }else{
            JOptionPane.showMessageDialog(rootPane, "No se encontro el valor solicitado");
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed

