    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int codigo = Integer.parseInt(jTextField1.getText());
        int res;
        if (jComboBox1.getSelectedIndex() == 0) {
            if ((res = main.equipoSonido.Buscar(codigo)) != -1) {
                JOptionPane.showMessageDialog(rootPane, main.equipoSonido.MostrarPos(res));
            } else {
                JOptionPane.showMessageDialog(rootPane, "No se encuentra el elemento");
            }
        } else {
            if (jComboBox1.getSelectedIndex() == 1) {
                if ((res = main.lavadora.Buscar(codigo)) != -1) {
                    JOptionPane.showMessageDialog(rootPane, main.lavadora.MostrarPos(res));
                } else {
                    JOptionPane.showMessageDialog(rootPane, "No se encuentra el elemento");
                }
            } else {
                if (jComboBox1.getSelectedIndex() == 2) {
                    if ((res = main.televisor.Buscar(codigo)) != -1) {
                        JOptionPane.showMessageDialog(rootPane, main.televisor.MostrarPos(res));
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "No se encuentra el elemento");
                    }
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

