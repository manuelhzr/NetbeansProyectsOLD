    private void Listar(){
        DefaultTableModel model =(DefaultTableModel) jTable1.getModel();
        
        for (int i = 0; i < Inventario.datosInventario.getPosicion(); i++) {
            Object[] NewRow = new Object[5];
            Producto actual = Inventario.datosInventario.getListaProductos(i);
            
            NewRow[0]=actual.getCodigo();
            NewRow[1]=actual.getNombre();
            NewRow[2]=actual.getCantidad();
            NewRow[3] =actual.getPrecioUnitario();
            NewRow[4]=actual.getPrecioLote();
            
            model.addRow(NewRow);
        }
        
        jTable1.setModel(model);
    }
    
