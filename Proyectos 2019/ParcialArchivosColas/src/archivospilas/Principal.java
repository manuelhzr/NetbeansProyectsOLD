    private void clearTable(JTable jTable) {
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        int a = jTable.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            model.removeRow(model.getRowCount() - 1);
        }
    }

    private void clear() {
        jTextField1.setText("");
        jTextField2.setText("");
        jComboBox1.setSelectedIndex(0);
        jTextField4.setText("");
        jTextField5.setText("");

        jTextField1.requestFocus();
    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            int cod = Integer.parseInt(jTextField1.getText());
            String nombre = jTextField2.getText();
            String sexo = jComboBox1.getSelectedItem().toString();
            String eps = jTextField4.getText();
            String fecha = jTextField5.getText();

            Persona nuevo = new Persona(cod, nombre, sexo, eps, fecha);
            if (archivo.guardarArchivo(nuevo)) {
                cola.agregar(nuevo);
                JOptionPane.showMessageDialog(rootPane, "Persona agregada");
            } else {
                JOptionPane.showMessageDialog(rootPane, "No se pudo agregar\nEl ID ya existe");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "La informacion es erronea");
        } catch (IOException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, "Error al leer el archivo");
        }
        clear();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTabbedPane1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jTabbedPane1StateChanged
        // TODO add your handling code here:
        if (jTabbedPane1.getSelectedIndex() == 1) {

            try {
                List<Persona> libros = archivo.leerArchivo();
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                clearTable(jTable1);
                for (Persona act : libros) {
                    model.addRow(new Object[]{act.getId(), act.getNombre(), act.getSexo(), act.getEps(), act.getFecha()});
                }
            } catch (IOException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(rootPane, "Error al leer el archivo");
            }
        }
    }//GEN-LAST:event_jTabbedPane1StateChanged

