    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        trabajador aux = new trabajador();
        aux.setNombre(this.jTextField2.getText());
        aux.setHoras(Integer.parseInt(this.jTextField1.getText()));
        aux.setValorHora(Float.parseFloat(this.jTextField3.getText()));
        aux.setSalario(Float.parseFloat(this.jTextField4.getText()));
        
        try {
            Vista.trabajadores.guardarArchivo(aux);
            JOptionPane.showMessageDialog(rootPane, "Agregado");
        } catch (IOException ex) {
            Logger.getLogger(llenar.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, "Error");
        }
        limpiar();
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void limpiar(){
        this.jTextField1.setText("");
        this.jTextField2.setText("");
        this.jTextField3.setText("");
        this.jTextField4.setText("");
    }
    
