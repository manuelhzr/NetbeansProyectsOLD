    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el ID del estudiante a buscar"));
        try {
            String res = materias.BuscarID(id);
            if (!res.equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Resultados:\n" + res);
            } else {
                JOptionPane.showMessageDialog(rootPane, "No se encontro el id");
            }
        } catch (IOException ex) {
            Logger.getLogger(Ventana.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, "Error");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        try {

            String nombre = JOptionPane.showInputDialog("Ingrese el nombre");
            String apellido = JOptionPane.showInputDialog("Ingrese el apellido");
            int id = Integer.parseInt(JOptionPane.showInputDialog("Ingrese la identificacion"));
            int telefono = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el telefono"));

            estudiantes est = new estudiantes(nombre, apellido, id, telefono);

            int idMateria = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el ID de la materia"));
            String nombreMateria = JOptionPane.showInputDialog("Ingrese el nombre de la materia");
            String horario = JOptionPane.showInputDialog("Ingrese el horario");

            materias mat = new materias(idMateria, nombreMateria, horario);

            estudiantes.guardarArchivo(est);
            materias.guardarArchivo(mat);

            JOptionPane.showMessageDialog(rootPane, "Datos Registrados");
        } catch (IOException ex) {
            Logger.getLogger(Ventana.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, "Error");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

