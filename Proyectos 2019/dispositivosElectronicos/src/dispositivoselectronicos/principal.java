    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        
        if (jComboBox1.getSelectedIndex()==0) {
            String items[]={"Portatil", "Escritorio"};
            TipoDisp.setText("Tipo de Computadora");
            Val1.setText("Almacenamiento");
            Val2.setVisible(true);
            Val2.setEnabled(true);
            ultimoCampo.setEnabled(true);
            ultimoCampo.setVisible(true);
            jComboBox2.setModel( new DefaultComboBoxModel<>(items));
        }else{
            String items[] = {"Inyeccion", "Tinta Continua"};
            TipoDisp.setText("Tipo de Impresora");
            Val1.setText("Conectividad");
            Val2.setEnabled(false);
            Val2.setVisible(false);
            ultimoCampo.setEnabled(false);
            ultimoCampo.setVisible(false);
            jComboBox2.setModel( new DefaultComboBoxModel<>(items) );
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jComboBox1.getSelectedIndex()==0) {
            computadora ob = new computadora(jComboBox2.getSelectedItem().toString(), Float.parseFloat(Camp1.getText()), Float.parseFloat(ultimoCampo.getText()));
            ob.setTipoDispositivo(jComboBox1.getSelectedItem().toString());
            ob.setMarca(marca.getText());
            ob.setModelo(modelo.getText());
            ob.setValAproximado(Float.parseFloat(valaprox.getText()));
            if (Computadora.setListaComputadoras(ob)) {
                JOptionPane.showMessageDialog(rootPane, "Valor Agregado");
            }else{
                JOptionPane.showMessageDialog(rootPane, "No se pudo agregar");
            }
        }else if (jComboBox1.getSelectedIndex()==1) {
           impresora ob= new impresora(jComboBox2.getSelectedItem().toString(), Camp1.getText());
           ob.setTipoDispositivo(jComboBox1.getSelectedItem().toString());
           ob.setMarca(marca.getText());
           ob.setModelo(modelo.getText());
           ob.setValAproximado(Float.parseFloat(valaprox.getText()));
            if (Impresora.setListaImpresoras(ob)) {
              JOptionPane.showMessageDialog(rootPane, "Valor Agregado");
            }else{
                JOptionPane.showMessageDialog(rootPane, "No se pudo agregar");
            }
        }
        
        Limpiar();
    }//GEN-LAST:event_jButton1ActionPerformed

